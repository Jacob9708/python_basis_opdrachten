c = -12
f = 102

# Celsius naar fahrenheit
fahrenheit = (c * 9/5) + 32

# Fahrenheit naar Celsius
celcius = (f - 32) * 5/9

# Toon de resultaten
print(f"{c}°C is gelijk aan {fahrenheit:.2f}°F")
print(f"{f}°F is gelijk aan {celcius:.2f}°C")

